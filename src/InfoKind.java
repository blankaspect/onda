// IMPORTS


import java.util.Set;

//----------------------------------------------------------------------


// INFORMATION KIND ENUMERATION


enum InfoKind
{

////////////////////////////////////////////////////////////////////////
//  Constants
////////////////////////////////////////////////////////////////////////

    NONE    ( "none" ),
    TITLE   ( "title" ),
    LOG     ( "log" ),
    RESULT  ( "result" ),
    ALL     ( "all" )
    {
        @Override
        public void addTo( Set<InfoKind> infoKinds )
        {
            infoKinds.add( TITLE );
            infoKinds.add( LOG );
            infoKinds.add( RESULT );
        }
    };

////////////////////////////////////////////////////////////////////////
//  Constructors
////////////////////////////////////////////////////////////////////////

    private InfoKind( String key )
    {
        this.key = key;
    }

    //------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Class methods
////////////////////////////////////////////////////////////////////////

    public static InfoKind forKey( String key )
    {
        for ( InfoKind value : values( ) )
        {
            if ( value.key.equals( key ) )
                return value;
        }
        return null;
    }

    //------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods
////////////////////////////////////////////////////////////////////////

    public void addTo( Set<InfoKind> infoKinds )
    {
        infoKinds.add( this );
    }

    //------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance variables
////////////////////////////////////////////////////////////////////////

    private String  key;

}

